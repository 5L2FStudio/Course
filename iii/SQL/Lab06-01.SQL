CREATE TABLE T1
(
	F1	INT,
	F2  NVARCHAR(10),
	F3  CHAR(10),
	F4  NCHAR(256)
)
GO

SET NOCOUNT ON ;

DECLARE @PTR INT = 1 ;
DECLARE @DATA NVARCHAR(10);

BEGIN TRAN
	WHILE ( @PTR <= 1000000 )
	BEGIN

		SET @DATA = RIGHT('0000000000'+CAST( @PTR AS NVARCHAR(10) ),10 )

		INSERT INTO T1(F1,F2,F3,F4)
			VALUES ( @PTR, @DATA , @DATA , NEWID() )

		SET @PTR += 1 ;
	END
COMMIT


SELECT * FROM T1
